package com.futor.analytics.domain;

public class StudentTransactionType {
	private long revisiontime=0;
	private long practicetime=0;
	private long studytime=0;
	public long getRevisiontime() {
		return revisiontime;
	}
	public void setRevisiontime(long revisiontime) {
		this.revisiontime = revisiontime;
	}
	public long getPracticetime() {
		return practicetime;
	}
	public void setPracticetime(long practicetime) {
		this.practicetime = practicetime;
	}
	public long getStudytime() {
		return studytime;
	}
	public void setStudytime(long studytime) {
		this.studytime = studytime;
	}
	
	
}
